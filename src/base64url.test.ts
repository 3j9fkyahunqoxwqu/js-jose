import {Base64Url} from './base64url';

it('base64url encodes', () => {
    const s = Base64Url.encode([0x17, 0xa0, 0xee, 0x33, 0x84, 0xfe, 0xfe, 0x64, 0xa4, 0x82, 0x0a]);
    expect(s).toBe("F6DuM4T-_mSkggo");
});

it('base64url decodes', () => {
    const s = Base64Url.decode("F6DuM4T-_mSkggo");
    expect(s).toEqual(new Uint8Array([0x17, 0xa0, 0xee, 0x33, 0x84, 0xfe, 0xfe, 0x64, 0xa4, 0x82, 0x0a]));
});